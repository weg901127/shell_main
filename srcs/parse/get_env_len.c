#include "../../micro_shell.h"

int	get_env_len(char *tmp)
{
	int	res;

	res = 0;
	if (!tmp)
		return (0);
	while (*tmp)
	{
		if (ft_isspace(*tmp) || *tmp == '\'' || *tmp == '\"' || *tmp == '$'
			|| *tmp == '|' || *tmp == '=' || *tmp == '/' || *tmp == '\\')
			break ;
		res++;
		tmp++;
	}
	return (res);
}
